public class CharacterPrinter {
    public static void main(String[] args) {
        int level;
        level = 1;
        System.out.println("Level: " + level);
        double health;
        health = 12.5;
        System.out.println("Health: " + health);
        String name;
        name = "Evan";
        System.out.println("Name: " + name);
    }
}
