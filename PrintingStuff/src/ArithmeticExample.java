public class ArithmeticExample {
    public static void main(String[] args) {
        int numberOne;
        numberOne = 10;
        int num2 = 20;
        numberOne += 10;
        numberOne--;
        num2 *= numberOne;
        num2++;
        System.out.println("Num 1 is: " + numberOne);
        System.out.println("Num 2 is: " + num2);

    }
}
